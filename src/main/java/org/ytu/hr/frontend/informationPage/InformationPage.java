    private void absentButtonActionPerformed(java.awt.event.ActionEvent evt) { // AbssnetDay
        if (employee.isIs_absent()) {
            JOptionPane.showMessageDialog(null, "Bu kişi bugün izindedir.", "Hata", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!isAbsent) {
            DayOff.addAbsentDay(employee);
            dayOffTextField.setText("" + employee.getAbsentDay());
            thisMonthSalaryTextField.setText(new Payment().reducedSalary(employee) + "");
            isAbsent = true;
        } else {
            JOptionPane.showMessageDialog(null, "Bugün bu kişinin işe gelmediğini belirttiniz.", "Hata", JOptionPane.ERROR_MESSAGE);
        }
    }
